////////////////////////////////////////////////////////////////////////////////

#include "textpage.h"

////////////////////////////////////////////////////////////////////////////////

#include <QTextEdit>
#include <QVBoxLayout>

////////////////////////////////////////////////////////////////////////////////


TextPage::TextPage( QWidget* _parent /* = NULL */ )
: QWizardPage(_parent)
{
    // Create text edit
    m_Text = new QTextEdit;

    // Create layout
    QVBoxLayout *layout = new QVBoxLayout;
    layout->addWidget(m_Text);

    // Register field
    registerField( "Text*", m_Text, "plainText", SIGNAL(textChanged()) );

    setLayout(layout);
}


////////////////////////////////////////////////////////////////////////////////


TextPage::~TextPage()
{
}


