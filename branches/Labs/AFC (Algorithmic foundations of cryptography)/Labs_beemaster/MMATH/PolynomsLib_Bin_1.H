#ifndef POLYNOMS_PROCEDURES_LIB
#define POLYNOMS_PROCEDURES_LIB

/* summ = a+b mod 2 (bitwise XOR)*/
void add_pol(M_LONG a,M_LONG b,M_LONG summ);

/*
ПРИВЕДЕНИЕ ПОЛИНОМА rem ПО МОДУЛЮ ПОЛИНОМА m
		rem = rem mod m
Параметры:
  rem - исходный приводимый полином (результат записывается сюда же);
  m - модуль (полином).
Выходное значение:
  нет.
*/
void rem_pol(M_LONG rem,M_LONG m);

/*
ВЫЧИСЛЕНИЕ ОСТАТКА rem ОТ ПРОИЗВЕДЕНИЯ ПОЛИНОМОВ a И b ПО МОДУЛЮ ПОЛИНОМА m
                          rem = a*b mod m
Параметры:
   a,b - исходные перемножаемые полиномы;
   m   - модуль (полином);
   rem - остаток от произведения полиномов a и b, приведенного по модулю m.
Выходное значение:
  нет.
*/
void mulmod_pol(M_LONG a,M_LONG b,M_LONG m,M_LONG rem);

/*
           ВЫЧИСЛЕНИЕ ПРОИЗВЕДЕНИЯ ПОЛИНОМОВ a И b
                          rem = a*b
Параметры:
   a,b - исходные перемножаемые полиномы;
   rem - произведение полиномов a и b.
Выходное значение:
  нет.
*/
void mul_pol(M_LONG a,M_LONG b,M_LONG rem);


/*
ВЫЧИСЛЕНИЕ КВАДРАТА ПОЛИНОМА a ПО МОДУЛЮ ПОЛИНОМА m
                          rem = a*a mod m
Параметры:
   a - исходные возводимый в квадрат полином;
   m   - модуль (полином);
   rem - остаток от вычисления квадрата полинома a, приведенного по модулю m.
Выходное значение:
  нет.
*/
void sqrmod_pol(M_LONG a,M_LONG m,M_LONG rem);

/*
ВЫЧИСЛЕНИЕ ОСТАТКА rem ОТ РЕЗУЛЬТАТА ВОЗВЕДЕНИЯ ПОЛИНОМА a В СТЕПЕНЬ b,
                   ПРИВЕДЕННОГО ПО МОДУЛЮ ПОЛИНОМА m
                          rem = a^b mod m
Параметры:
   a   - исходныq возводимый в степень полином;
   m   - модуль (полином);
   rem - остаток от результата возведения полинома a в степень b, приведенного по модулю m.
Выходное значение:
  нет.
*/
void pow_pol(M_LONG a,M_LONG b,M_LONG m,M_LONG rem);

/*
                  ДЕЛЕНИЕ ПОЛИНОМОВ
                 r = a / m; s =  a % m;
Параметры:
  a - полином-делимое;
  m - полином-делитель;
  r - целая часть от деления (полином);
  s - остаток от деления (полином).
Выходное значение:
  нет.//????
*/
void div_pol(M_LONG a,M_LONG m,M_LONG r,M_LONG s);

/*
ВЫЧИСЛЕНИЕ МУЛЬТИПЛИКАТИВНО ОБРАТНОГО ЭЛЕМЕНТА inv ОТ ПОЛИНОМА g ПО МОДУЛЮ ПОЛИНОМА m
                          inv = g^(-1) mod m
Параметры:
   g   - заданный полином;
   m   - модуль (полином);
   inv - мультипликативно обратный элемент (полином) для g по модулю m.
Выходное значение:
  0 - обратный элемент успешно найден;
  1 - обратный элемент не найден (не взаимно простые полиномы (g,m)!=1).
*/
int inv_pol(M_LONG g,M_LONG m,M_LONG inv);

#endif