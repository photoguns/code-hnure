#ifndef _CODER_H_
#define _CODER_H_
////////////////////////////////////////////////////////////////////////////////

#include <string>

////////////////////////////////////////////////////////////////////////////////

class Container;
class Key;

////////////////////////////////////////////////////////////////////////////////


/** C++ interface class for stegano -coding -encoding
*    
*
*  @author  Roman Pasechnik
*  @since   March 25th, 2009
*  @updated March 25th, 2009
*
*/
class Coder
{

////////////////////////////////////////////////////////////////////////////////

public:

////////////////////////////////////////////////////////////////////////////////


    /** Constructor */
	Coder();


    /** Destructor */
    virtual ~Coder();


    /** Puts the message into container */
    virtual void Encrypt ( const std::string& _message,
                           Container* _container,
                           const Key* _key = NULL ) = 0;


    /** Gets the message from container */
    virtual std::string Decrypt ( const Container* _container,
                                  const Key* _key = NULL ) = 0;


////////////////////////////////////////////////////////////////////////////////

private:

////////////////////////////////////////////////////////////////////////////////
};
#endif //_CODER_H_
